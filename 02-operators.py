# arithmetic operators
print("arithmetic operators")
print("1 + 3 = ", 1 + 3)
print("4 - 2 = ", 4 - 2)
print("1 * 11 = ", 1 * 11)
print("5 / 2 = ", 5 / 2)

# assignment operators 
print("assignment operators")
a = 14
print(a)

a += 2
print("14 + 2 = ", a)

a -= 11
print("16 - 11 = ", a)

a *= 5
print("5 * 5 = ", a)

a /= 2
print("25 / 2 = ", a)

# comparison operators
print("comparison operators")
print("2 > 4 : ", 2 > 4)
print("2 < 4 : ", 2 < 4)
print("5 >= 5 : ", 5 >= 5)
print("5 <= 5 : ", 5 <= 5)
print("2 == 2 : ", 2 == 2)
print("2 != 2 : ", 2 != 2)

# logical operators ( operates on a booleans)
print("logical operators")
boolean_1 = True
boolean_2 = False

print(boolean_1, " and", boolean_2, " : ", (boolean_1 and boolean_2))
print(boolean_1, " or", boolean_2, " : ", (boolean_1 or boolean_2))
print("not ", boolean_1, " : ", (not boolean_1))
print("not ", boolean_2, " : ", (not boolean_2))